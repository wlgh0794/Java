public class _02_middle {

	public static void main(String[] args) {
		
		/* while문을 이용해서 1부터 10까지 중에 짝수만 출력하세요. */
		
		int num = 1 ;
		
		while( num <= 10 )
		{
			
			if ( num %2 == 0 )
			{
				
				System.out.println( " 값 :  " +  num  + " 짝수 입니다. " );
				
				
			}
			
			num++ ;
			
			
			
		}
		
		
		

	}

}
